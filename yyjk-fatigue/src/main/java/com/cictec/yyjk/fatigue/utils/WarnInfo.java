package com.cictec.yyjk.fatigue.utils;

public class WarnInfo {
	private Integer staSqn;
	private String devCode;
	private Integer onNumber;
	private Integer offNumber;

	public Integer getStaSqn() {
		return staSqn;
	}

	public void setStaSqn(Integer staSqn) {
		this.staSqn = staSqn;
	}

	public String getDevCode() {
		return devCode;
	}

	public void setDevCode(String devCode) {
		this.devCode = devCode;
	}

	public Integer getOnNumber() {
		return onNumber;
	}

	public void setOnNumber(Integer onNumber) {
		this.onNumber = onNumber;
	}

	public Integer getOffNumber() {
		return offNumber;
	}

	public void setOffNumber(Integer offNumber) {
		this.offNumber = offNumber;
	}

}
