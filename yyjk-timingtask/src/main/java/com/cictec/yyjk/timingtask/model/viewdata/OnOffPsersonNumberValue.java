package com.cictec.yyjk.timingtask.model.viewdata;

public class OnOffPsersonNumberValue {
	private String orgName;
	private String lineName;
	private String uploadTime;
	private String onPersonCount;
	private String offPersonCount;

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public String getLineName() {
		return lineName;
	}

	public void setLineName(String lineName) {
		this.lineName = lineName;
	}

	public String getUploadTime() {
		return uploadTime;
	}

	public void setUploadTime(String uploadTime) {
		this.uploadTime = uploadTime;
	}

	public String getOnPersonCount() {
		return onPersonCount;
	}

	public void setOnPersonCount(String onPersonCount) {
		this.onPersonCount = onPersonCount;
	}

	public String getOffPersonCount() {
		return offPersonCount;
	}

	public void setOffPersonCount(String offPersonCount) {
		this.offPersonCount = offPersonCount;
	}

}
